public class Prettyprinting {
    public static void main(String[] args) {
        float a=453.1234f;
        System.out.printf("Formatted number is %.2f",a);
        System.out.println();
        System.out.printf("Pie : %.3f",Math.PI);
        System.out.println();
        System.out.printf("My name is %s and my title is %s","tridib","ghosh");
    }
}
